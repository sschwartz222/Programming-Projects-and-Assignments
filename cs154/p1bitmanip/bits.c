/* howManyBits - return the minimum number of bits required to represent x in
 *             two's complement
 *  Examples: howManyBits(12) = 5
 *            howManyBits(298) = 10
 *            howManyBits(-5) = 4
 *            howManyBits(0)  = 1
 *            howManyBits(-1) = 1
 *            howManyBits(0x80000000) = 32
 *  Legal ops: ! ~ & ^ | + << >>
 *  Max ops: 90
 *  Rating: 4
 */
int howManyBits(int x) {
  // use binary search!
  x = (((x >> 31) & ~x) | (~(x >> 31) & x));
  int shift16 = ((!!(x >> 16)) << 4);
  x = x >> shift16;
  int shift8 = ((!!(x >> 8)) << 3);
  x = x >> shift8;
  int shift4 = ((!!(x >> 4)) << 2);
  x = x >> shift4;
  int shift2 = ((!!(x >> 2)) << 1);
  x = x >> shift2;
  int shift1 = (!!(x >> 1));
  x = x >> shift1;
  int shift0 = x;
  return (shift16 + shift8 + shift4 + shift2 + shift1 + shift0 + 1);
}

