/*
 * isGreater - if x > y  then return 1, else return 0 
 *   Example: isGreater(4,5) = 0, isGreater(5,4) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 24
 *   Rating: 3
 */
int isGreater(int x, int y) {
  int result = (!((x + (~y + 1)) >> 31));
  int xsign = x >> 31;
  int ysign = y >> 31;
  return ((((!xsign) & ysign) | (result & ((!xsign) | ysign))) & (!!(x ^ y)));
}

